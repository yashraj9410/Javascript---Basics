/*
The history object in JavaScript provides an interface to the browser's session history,
 allowing you to navigate between previously visited pages. 
It allows you to manipulate the browser's history by adding, modifying, or removing entries.
*/

// examples 

// Move back one page in the browser history
history.back();



// Move forward one page in the browser history
history.forward();

// Go back two pages in the browser history
history.go(-2);

// Go forward three pages in the browser history
history.go(3);